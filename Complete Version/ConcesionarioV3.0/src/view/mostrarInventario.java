    private void mostrar() {
        this.model.addColumn("Placa");
        this.model.addColumn("Modelo");
        this.model.addColumn("Color");
        this.model.addColumn("NoMotor");
        this.model.addColumn("Ciudad");
        this.model.addColumn("Marca");
        this.model.addColumn("Tipo");
        //this.model.addColumn("Imagen");
        tablaInventario.setModel(model);
        //Creo el objeto de tipo inventarioCarrosCr
        Pila<inventarioCarrosCr> datos = r.mostrarinventario();

        if (datos != null) {
            //Creo las filas de la tabla
            Object[] filas = new Object[8];
            try {
                //Recorro el tama√±o de la pila
                for (int i = 0; i < datos.size(); i++) {
                    //Asigno el valor de la pila en cada variable a 
                    //cada pocision de la tabla
                    filas[0] = (datos.get(i).getPlaca());
                    filas[1] = (datos.get(i).getModelo());
                    filas[2] = (datos.get(i).getColor());
                    filas[3] = (datos.get(i).getNoMotor());
                    filas[4] = (datos.get(i).getCiudad());
                    filas[5] = (datos.get(i).getMarca());
                    filas[6] = (datos.get(i).getTipoc());
                    //filas[7] = (datos.get(i).getImagen());
                    model.addRow(filas);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al consultar los datos!!");
                e.printStackTrace();
                e.getLocalizedMessage();
            }
        }
    }

    private void borrar() {
        try {
            String dato = txtSearch.getText();

            Pila<inventarioCarrosCr> borrar = r.eliminarInventario(dato);

            if (borrar != null) {
                int i = 0;
                for (; i < borrar.size(); i--) {
                    borrar.remove(i).getPlaca();
                    borrar.remove(i).getModelo();
                    borrar.remove(i).getColor();
                    borrar.remove(i).getNoMotor();
                    borrar.remove(i).getCiudad();
                    borrar.remove(i).getMarca();
                    borrar.remove(i).getTipoc();

                    lblTamanio.setText(String.valueOf(i));

                    System.out.println("DATOS BORRADOS!");
                    System.out.println(borrar.remove(i).getPlaca());
                    System.out.println(borrar.remove(i).getModelo());
                    System.out.println(borrar.remove(i).getColor());
                    System.out.println(borrar.get(i).getNoMotor());
                    System.out.println(borrar.get(i).getCiudad());
                }

            }

        } catch (Exception ex) {
            //JOptionPane.showMessageDialog(null, "Error inesperado, estamos trabajando en ello!", "Error", JOptionPane.ERROR_MESSAGE);
            //System.out.println("Este error es de la vista " + ex.getMessage() + "  \n" + ex.getLocalizedMessage());
        }
    }

    private void filtro() {
        txtSearch.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent ke) {
                trs.setRowFilter(RowFilter.regexFilter("(?i)" + txtSearch.getText(), 0));
            }
        });
        trs = new TableRowSorter(model);
        tablaInventario.setRowSorter(trs);
    }

